class PromptTemplates:
    _SYSTEM = (
        "Ты опытный тренер по волейболу. Давай краткие, конкретные советы, "
        "учитывая счёт и сет. Если команда ведёт с большим отрывом, советуй "
        "не сбавлять темп. Если счёт равный — посоветуй тактические ходы. "
        "Не давай общих фраз. Старайся уложиться в два-три предложения."
    )

    def get(self, context: dict) -> str:
        prompt = (
            f"Сейчас счёт {context['score_a']}:{context['score_b']} "
            f"в {context['current_set']} сете. Команда {context['team_a']}. "
            f"Какой совет дашь?"
        )
        return f"{self._SYSTEM}\n\n{prompt}"
